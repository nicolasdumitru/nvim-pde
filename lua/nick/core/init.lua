vim.opt.incsearch = true

-- Minimum amount of lines above and below cursor
vim.opt.scrolloff = 4

-- Splits
vim.opt.splitright = true
vim.opt.splitbelow = true

-- Tildeop
vim.opt.tildeop = false

-- Folds
vim.opt.foldenable = false
